import numpy as np
#4 * 3 empty and full numpy array
a = np.empty((4,3))
print("Empty numpy array is::")
print(a)
b = np.full((4,3),22)
print("Full numpy array is::")
print(b)
#3*5 array filled all with zeros
c = np.zeros((3,5))
print("A array filled with 0::")
print(c)
#4*3*2 array filled with ones
d = np.ones((4,3,2))
print("An array filled with one::")
print(d)